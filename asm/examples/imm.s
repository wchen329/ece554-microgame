# Immediate Examples
# -

addi $s0, $s0, h29		# Hex 29
addi $s0, $s0, o7		# Octal 7
addi $s0, $s0, b11111111	# 11111111
addi $s0, $s0, 30		# 30
addi $s0, $s0, -1		# -1
addi $s0, $s0, h-ffffffff	# Negation of ffffffff (i.e. in two's complement) so, 1
addi $s0, $s0, b-1111111	# Negation of 1111111 (i.e. in two's complement)
